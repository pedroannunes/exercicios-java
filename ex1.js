let value1 = parseInt(prompt("Insira o primeiro valor: "));
let value2 = parseInt(prompt("Insira o segundo valor: "));
var invalid = 0;

while (value2 <= invalid) {
    alert("O seu número é inválido, insira um número maior que zero!");
    value2 = parseInt(prompt("Insira o segundo valor: "));
}

alert(`A sua divisão é igual a ${value1 / value2}`);